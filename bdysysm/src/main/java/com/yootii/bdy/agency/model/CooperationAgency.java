package com.yootii.bdy.agency.model;

public class CooperationAgency {
    private Integer id;

    private Integer agencyId;

    private Integer cooperationAgencyId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getAgencyId() {
        return agencyId;
    }

    public void setAgencyId(Integer agencyId) {
        this.agencyId = agencyId;
    }

    public Integer getCooperationAgencyId() {
        return cooperationAgencyId;
    }

    public void setCooperationAgencyId(Integer cooperationAgencyId) {
        this.cooperationAgencyId = cooperationAgencyId;
    }
}